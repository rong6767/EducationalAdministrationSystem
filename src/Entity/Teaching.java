public class Teaching {
    String Tno;
    String Courseno;
    int Tday;
    int Tserial;
    int Teachingno;

    public int getTeachingno() {
        return Teachingno;
    }

    public void setTeachingno(int teachingno) {
        Teachingno = teachingno;
    }

    public int getTday() {
        return Tday;
    }

    public void setTday(int tday) {
        Tday = tday;
    }

    public int getTserial() {
        return Tserial;
    }

    public void setTserial(int tserial) {
        Tserial = tserial;
    }

    public String getTno() {
        return Tno;
    }

    public void setTno(String tno) {
        Tno = tno;
    }

    public String getCourseno() {
        return Courseno;
    }

    public void setCourseno(String courseno) {
        Courseno = courseno;
    }


}
